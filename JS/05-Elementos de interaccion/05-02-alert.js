/*
NOTA:alert() muestra un diálogo de alerta con un mensaje opcional, y 
aguardará hasta que el usuario cierre la ventana de diálogo.

este alert amuestra en pantalla una pequeña ventana con un mensaje
el que le coloquemos dentro de la función.

La mini ventana con el mensaje se llama * ventana modal *. La palabra
“modal” significa que el visitante no puede interactuar con el resto 
de la página, presionar otros botones, etc., hasta que se haya ocupado 
de la ventana. En este caso, hasta que presionen “OK”.

Por ejemplo:
*/

alert('Esto es un mensaje');

/*
NOTA: Podemos enviar mensajes como string y también podemos pasar variables
y mostrarlas dentro de la función.

Por ejemplo:
*/
let mensaje;
mensaje = 'Esto es un mensaje'
alert(mensaje);
